# ----- Integer & Float -----
print(2 + 5)
print(2 - 5)
print(2 * 5)
print(2 / 5)

print(type(2 + 5)) # int
print(type(2 / 5)) # float
print(type(0.00001)) # float

# float takes up more memory than int

print(type(20 + 1.1)) # converts to float

# power of
print(2 ** 3) # 8

# double divide
print(2 // 4) # 0

# modulo
print(5 % 4) # 1

# ----- Math functions -----
print(round(3.1))
print(abs(-20)) # absolute value, pos numbers